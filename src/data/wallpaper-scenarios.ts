export type Lock = 'rollWidth' | 'rollLength' | 'rapport';

export interface Scenario {
  slug: string;
  category: 'area' | 'room-type' | 'roll-type' | 'feature' | 'combo';
  h1: string;
  title: string;
  description: string;
  prefill: {
    length?: number;
    width?: number;
    height?: number;
    rollWidth?: number;
    rollLength?: number;
    rapport?: number;
    doors?: number;
    windows?: number;
  };
  lock?: Lock[];
  // Уникальный вводный абзац (НЕ генерируется ИИ по шаблону, а пишется через
  // builder ниже — гарантирует осмысленный текст с реальными цифрами).
  introBlocks: string[];
}

// Подбираем размеры комнаты под её площадь, чтобы пропорции были правдоподобными.
function dimsForArea(area: number): { length: number; width: number } {
  const ratio = Math.sqrt(area / 1.5);
  const width = Math.round(ratio * 10) / 10;
  const length = Math.round((area / width) * 10) / 10;
  return { length, width };
}

