export type InsulationLock = 'thickness' | 'surface' | 'packArea';

export interface InsulationScenario {
  slug: string;
  category: 'area' | 'surface' | 'material' | 'thickness';
  h1: string;
  title: string;
  description: string;
  prefill: {
    length?: number;
    width?: number;
    height?: number;
    thickness?: number;
    surface?: 'walls' | 'roof' | 'floor' | 'all';
    packArea?: number;
    doors?: number;
    windows?: number;
  };
  lock?: InsulationLock[];
  introBlocks: string[];
}

function dimsForArea(area: number) {
  const ratio = Math.sqrt(area / 1.5);
  const width = Math.round(ratio * 10) / 10;
  const length = Math.round((area / width) * 10) / 10;
  return { length, width };
}

