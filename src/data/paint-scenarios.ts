export type PaintLock = 'surface' | 'layers' | 'coverage';

export interface PaintScenario {
  slug: string;
  category: 'area' | 'room-type' | 'surface' | 'paint-type' | 'combo';
  h1: string;
  title: string;
  description: string;
  prefill: {
    length?: number;
    width?: number;
    height?: number;
    surface?: 'walls' | 'ceiling' | 'walls-ceiling';
    layers?: number;
    coverage?: number;
    doors?: number;
    windows?: number;
  };
  lock?: PaintLock[];
  introBlocks: string[];
}

function dimsForArea(area: number) {
  const ratio = Math.sqrt(area / 1.5);
  const width = Math.round(ratio * 10) / 10;
  const length = Math.round((area / width) * 10) / 10;
  return { length, width };
}

