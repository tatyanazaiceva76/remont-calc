export type TileLock = 'tileLength' | 'tileWidth' | 'layout' | 'groutWidth';

export interface TileScenario {
  slug: string;
  category: 'area' | 'room-type' | 'size' | 'layout' | 'combo';
  h1: string;
  title: string;
  description: string;
  prefill: {
    length?: number;
    width?: number;
    tileLength?: number;
    tileWidth?: number;
    layout?: 'straight' | 'diagonal' | 'brick';
    groutWidth?: number;
  };
  lock?: TileLock[];
  introBlocks: string[];
}

function dimsForArea(area: number) {
  const ratio = Math.sqrt(area / 1.2);
  const width = Math.round(ratio * 10) / 10;
  const length = Math.round((area / width) * 10) / 10;
  return { length, width };
}

