// Сценарии расчёта ламината — каждый объект = одна страница /raschet-laminata/{slug}/

export type LamLock = 'panelLength' | 'panelWidth' | 'panelsPerPack' | 'layout';

export interface LaminateScenario {
  slug: string;
  category: 'area' | 'room-type' | 'panel-type' | 'layout' | 'combo';
  h1: string;
  title: string;
  description: string;
  prefill: {
    length?: number;
    width?: number;
    panelLength?: number;
    panelWidth?: number;
    panelsPerPack?: number;
    layout?: 'straight' | 'diagonal' | 'herringbone';
  };
  lock?: LamLock[];
  introBlocks: string[];
}

function dimsForArea(area: number) {
  const ratio = Math.sqrt(area / 1.5);
  const width = Math.round(ratio * 10) / 10;
  const length = Math.round((area / width) * 10) / 10;
  return { length, width };
}

