export type PlasterLock = 'thickness' | 'consumption' | 'bagWeight';

export interface PlasterScenario {
  slug: string;
  category: 'area' | 'room-type' | 'mix-type' | 'thickness';
  h1: string;
  title: string;
  description: string;
  prefill: {
    length?: number;
    width?: number;
    height?: number;
    thickness?: number;
    consumption?: number;
    bagWeight?: number;
    doors?: number;
    windows?: number;
  };
  lock?: PlasterLock[];
  introBlocks: string[];
}

function dimsForArea(area: number) {
  const ratio = Math.sqrt(area / 1.5);
  const width = Math.round(ratio * 10) / 10;
  const length = Math.round((area / width) * 10) / 10;
  return { length, width };
}

